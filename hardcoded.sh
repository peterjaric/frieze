get_text() {
    local prompt="$1"
    local -a suggestions
    # Check if prompt contains the word "slogans"
    if [[ "$prompt" == *"slogans"* ]]; then
        suggestions=("${SLOGAN_SUGGESTIONS[@]}")
        elif [[ "$prompt" == *"styles"* ]]; then
        suggestions=("${STYLE_SUGGESTIONS[@]}")
        elif [[ "$prompt" == *"motif"* ]]; then
        suggestions=("${MOTIF_SUGGESTIONS[@]}")
    else
        suggestions=("unknown")
    fi


    local random_index=$((RANDOM % ${#suggestions[@]}))
    echo "${suggestions[$random_index]}"
}
